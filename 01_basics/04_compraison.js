// ==========================================
// JavaScript Comparison Operators
// ==========================================

// Comparison operators compare two values.
// They always return a Boolean value:
// true  -> Condition is correct
// false -> Condition is not correct

/*
//  ==========================================
// 1. Equal To (==)
// Compares only the values (ignores data type)
// ==========================================
console.log("1. Equal To (==)");
console.log(5 == 5);       // true
console.log(5 == "5");     // true (string converted to number)
console.log(10 == 20);     // false
*/



/*
// ==========================================
// 2. Strict Equal To (===)
// Compares both value and data type
// ==========================================
console.log("\n2. Strict Equal To (===)");
console.log(5 === 5);      // true
console.log(5 === "5");    // false (number !== string)
console.log("Hello" === "Hello"); // true
*/




/*
// ==========================================
// 3. Not Equal (!=)
// ==========================================
console.log("\n3. Not Equal (!=)");
console.log(5 != 3);       // true
console.log(5 != "5");     // false
*/





/*
// ==========================================
// 4. Strict Not Equal (!==)
// ==========================================
console.log("\n4. Strict Not Equal (!==)");
console.log(5 !== "5");    // true
console.log(5 !== 5);      // false
*/




/*
// ==========================================
// 5. Greater Than (>)
// ==========================================
console.log("\n5. Greater Than (>)");
console.log(20 > 10);      // true
console.log(5 > 10);       // false
*/



/*
// ==========================================
// 6. Less Than (<)
// ==========================================
console.log("\n6. Less Than (<)");
console.log(5 < 10);       // true
console.log(15 < 10);      // false
*/




/*
// ==========================================
// 7. Greater Than or Equal (>=)
// ==========================================
console.log("\n7. Greater Than or Equal (>=)");
console.log(10 >= 10);     // true
console.log(15 >= 10);     // true
console.log(5 >= 10);      // false
*/







/*
// ==========================================
// 8. Less Than or Equal (<=)
// ==========================================
console.log("\n8. Less Than or Equal (<=)");
console.log(5 <= 10);      // true
console.log(10 <= 10);     // true
console.log(15 <= 10);     // false
*/
